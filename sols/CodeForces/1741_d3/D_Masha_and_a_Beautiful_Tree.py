def solve(N, A):
    A = [a - 1 for a in A]
    res = 0
    ok = 1

    @bootstrap
    def dfs(l, r, A):
        nonlocal res, ok
        if l == r - 1:
            if A[l] != l: ok = 0
        elif l < r:
            m = (l + r) // 2
            if l not in A[l:m]:
                res += 1
                A[l:r] = A[m:r] + A[l:m]
            yield dfs(l, m, A)
            yield dfs(m, r, A)
        yield None

    dfs(0, N, A)
    return res if ok else -1


def main():
    T = int(input())
    for _ in range(T):
        N = int(input())
        A = list(map(int, input().split()))
        res = solve(N, A)
        print(res)


if __name__ == '__main__':
    main()

