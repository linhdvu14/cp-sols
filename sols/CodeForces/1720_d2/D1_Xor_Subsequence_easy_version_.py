# dp[i] = 1 + max_(j < i, a[i]^j > a[j]^i) dp[j]
# a[j]^i >= i with last 8 bits unset
# --> j must share same prefix with i except possibly last 8 bits

def solve(N, A):
    dp = [1] * N

    for i in range(N):
        for j in range(max(0, i-256), i):
            if A[i]^j > A[j]^i:
                dp[i] = max(dp[i], 1 + dp[j])

    return max(dp)


def main():
    T = int(input())
    for _ in range(T):
        N = int(input())
        A = list(map(int, input().split()))
        out = solve(N, A)
        print(out)


